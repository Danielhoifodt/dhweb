<h2>Teknologien, og alle valgene - 23.06.2021</h2>
<p>Etter flere år med programmering har jeg tenkt en tanke. Blir teknologi bra om man gjør det bare for teknologiens skyld, eller må det noe mer til?
<br><br>Jeg som mange andre boknerder, liker å forstå systemer og hvordan ting fungerer bak kulissene. Men hva med helhetsinntrykket? Og har det noe verdi
det man lager? Man skjønner for hvor kjekt teknologi kan være når man virkelig får bruk for noe. Nettbanken, mailen eller noe annet. Men er du ikke enig at det
finnes mye fjas der ute. Ting som kanskje heller man kunne klart seg uten. Det gjør i hverfall jeg. Se på cloud hosting for å ta et eksempel. Amazon Web Services, Azure
, Google Cloud pluss en haug andre. Alle gjør samme nytten. Men konkurrerer og skaper et inntrykk av at èn er mye bedre enn en annen. Man trenger jo strengt tatt ikke
alle disse mulighetene, men de finnes og gjør kanskje at man kan velge etter favoritt. Men om du tror på all hypen som er der ute ender du opp i en evig runddans.
Da er det kanskje kjekt å ha litt basic forståelse av hosting før man setter i gang. Eller så er det lett å bli bitt av basillen om at noe er så mye bedre enn noe annet
    før du egentlig skjønner forskjellen.
<br><br>
Så om du har blitt hekta på teknologi, er vel dette en slags felle du har gått inn i. En evig runddans om at du vil nå et mål, men egentlig bare rører i grøten. Det
    kan ta deg flere år å finne ut av, men tro meg, jeg har vært der.<br>
Så vi liker muligheter kanskje, noen vil ha windows andre vil ha mac. Og noen sverger til Linux, for det er jo så mye lettere... Men et godt program kan lages på de
fleste operativsystemer, og du trenger ikke 20 000 kroners pc. Det er engentlig ganske enkelt og banalt. Men vi ødelegger alt ved å hele tiden reklamere og konkurrere.
Man kan også få seg en heftig økonomisk smell om man lett blir fristet av gode velvalgte reklameord. Jeg har gått i fellen før, men tenker ofte det at open source
programmvare er det beste. Så det er ofte jeg har gjort kompromisser og gått for en
gratisversjon og stort sett blitt fornøyd og fikk jobben gjordt.<br> Så før du velger å kjøpe et program neste gang tenk på alle mulighetene som finnes av freeware/open source
    og heller støtt de om du vil. F.eks Open Office/Office 365, GIMP/Photoshop, Linux/Windows eller Mac, brukt PC/ny. Listen er lang.</p>


